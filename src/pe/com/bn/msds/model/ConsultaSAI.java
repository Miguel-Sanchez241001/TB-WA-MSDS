package pe.com.bn.msds.model;

public class ConsultaSAI {

	private String codUsuario;
	private String codEmpleado;
	private String nomusuario;
	private String codDepend;
	private String nomDependen;
	
	public ConsultaSAI() {
	}

	public String getCodUsuario() {
		return codUsuario;
	}

	public void setCodUsuario(String codUsuario) {
		this.codUsuario = codUsuario;
	}

	public String getCodEmpleado() {
		return codEmpleado;
	}

	public void setCodEmpleado(String codEmpleado) {
		this.codEmpleado = codEmpleado;
	}

	public String getNomusuario() {
		return nomusuario;
	}

	public void setNomusuario(String nomusuario) {
		this.nomusuario = nomusuario;
	}

	public String getCodDepend() {
		return codDepend;
	}

	public void setCodDepend(String codDepend) {
		this.codDepend = codDepend;
	}

	public String getNomDependen() {
		return nomDependen;
	}

	public void setNomDependen(String nomDependen) {
		this.nomDependen = nomDependen;
	}
	
}
